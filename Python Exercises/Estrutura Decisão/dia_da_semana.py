dia = int(input("Informe o dia da semana: (sendo 1 - domingo, 2 - segunda, ..., 7 - sábado "))

match dia:
    case 1:
        print ("Domingo")
    case 2:
        print ("Segunda")
    case 3:
        print ("Terça")
    case 4:
        print ("Quarta")
    case 5:
        print ("Quinta")
    case 6:
        print ("Sexta")
    case 7:
        print ("Sábado")